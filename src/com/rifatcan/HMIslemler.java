package com.rifatcan;

public class HMIslemler 
{
	public double toplama (double s1 , double s2)
	{
		return s1 + s2;
	}
	
	public double cýkartma (double s1 , double s2)
	{
		return s1 - s2;
	}
	
	public double carpma (double s1 , double s2)
	{
		return s1 * s2;
	}
	
	public double bolme (double s1 , double s2)
	{
		return s1 / s2;
	}
	
	public double mod (double s1 , double s2)
	{
		return s1 % s2;
	}
	
	public double kareal (double s1)
	{
		return s1 * s1;
	}
}

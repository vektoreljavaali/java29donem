package com.Hocam;

public class HMIslemler {

	public double toplama(double s1,double s2) {
		return s1+s2;
	}
	
	public double cikartma(double s1,double s2) {
		return s1-s2;
	}
	
	public double carpma(double s1,double s2) {
		return s1*s2;
	}
	
	public double bolme(double s1,double s2) {
		return s1/s2;
	}
	
}
